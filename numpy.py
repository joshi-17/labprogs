import numpy as np
import sympy as sp
basis1=np.array([(1,1,1),(1,1,0),(1,0,0)])
basis2=np.array([(1,3),(2,5)])
b2=np.transpose(basis2)
x=np.matrix([(1,1,1)])
x1=np.transpose(x)
y=np.matrix([(1,1,0)])
y1=np.transpose(y)
z=np.matrix([(1,0,0)])
z1=np.transpose(z)
lt=np.matrix([[3,2,-4],[1,-5,3]])
t111=np.dot(lt,x1)
t110=np.dot(lt,y1)
t100=np.dot(lt,z1)
a=np.linalg.solve(b2,t111)
b=np.linalg.solve(b2,t110)
c=np.linalg.solve(b2,t100)
print(a)
print(b)
print(c)
mt = np.row_stack((a.T, b.T, c.T))
mtr=np.transpose(mt)
print("matrix of linear transformation is eqaul to:\n", mtr)
